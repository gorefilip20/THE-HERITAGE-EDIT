const DEFAULT_LOCALE: LocaleConfig = {
  currency: "NGN",
  locale: "en-NG",
  language: "en",
  languageLabel: "English",
  countryCode: "NG",
};

export function getLocaleForCountry(countryCode: string): LocaleConfig {
  return COUNTRY_LOCALES[countryCode.toUpperCase()] ?? DEFAULT_LOCALE;
}

export function getDefaultLocale(): LocaleConfig {
  return DEFAULT_LOCALE;
}

export function getAllSupportedLocales(): LocaleConfig[] {
  return Object.values(COUNTRY_LOCALES);
}

export function formatLocalizedPrice(
  cents: number,
  currency: string,
  locale: string,
): string {
  return new Intl.NumberFormat(locale, {
    style: "currency",
    currency,
    minimumFractionDigits: 0,
    maximumFractionDigits: currency === "JPY" || currency === "KRW" ? 0 : 2,
  }).format(cents / 100);
}

